// C Variables
#include <stdio.h>
int main()
{
    int age = 25;
    printf("Age: %d", age);

    age = 32;
    printf("\nNew Age: %d", age);

    int number = 33;
    printf("\nNumber is = %d", number);

    int second_number = number;
    printf("\nSecond Number is = %d", second_number);

    // Multiple variables
    int var1 = 24;
    printf("\n%d", var1);

    return 0;
}

