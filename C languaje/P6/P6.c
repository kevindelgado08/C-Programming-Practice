// C Operators

#include <stdio.h>

int main (){

    /* Operadores aritmeticos

            + Addition
            - Substraction
            * Multiplication
            / Division
            % Remainder
            ++ Increment by 1
            -- Decrement by 1
    */

    //Addition
    int a = 12;
    int result = a + 1;

    printf("%d", result);

    printf("\n%d", a+1);

    double a1 = 12.35;
    double result1 = a1 + 8.87;

    printf("\n%.2lf", result1);


    // Division
    double a2 = 12.00;
    double result2 = a/8.00;

    printf("\n%.2lf", result2);


    // Module %

    return 0;
}
